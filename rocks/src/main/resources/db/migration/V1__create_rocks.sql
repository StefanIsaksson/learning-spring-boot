CREATE TABLE ROCK(
                        	UNIQUEKEY INT AUTO_INCREMENT,
                        	NAME VARCHAR(255),
                        	ROCK_TYPE VARCHAR(50),
                        	VISUAL_DESCRIPTION VARCHAR(2000),
                        	PICTURE_URL VARCHAR(200),
                        	ROCK_FORMATION VARCHAR(2000),
                        	MINERAL_COMPOSITION VARCHAR(2000),
                        	LOCATION VARCHAR(2000),
                        	MOHS_SCALE INT
                        );